-- Hàm kiểm tra xem user hiện tại có phải Admin hoặc Teacher không
-- [SECURITY FIX]: Thêm 'SET search_path = public' để chống chiếm quyền (Privilege Escalation)
CREATE OR REPLACE FUNCTION public.is_admin_or_teacher()
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id = auth.uid() 
    AND role IN ('system_admin', 'teacher')
  );
END;
$$;

