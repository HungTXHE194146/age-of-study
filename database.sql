-- ==============================================================================
-- PHẦN 4: TRIGGER TỰ ĐỘNG TẠO USER PROFILE
-- ==============================================================================

-- [SECURITY FIX]: 
-- 1. Luôn set role mặc định là 'student'. Không tin tưởng metadata từ Client gửi lên.
-- 2. Handle trường hợp thiếu username/fullname để không bị lỗi.
-- 3. Handle ON CONFLICT để không abort auth.users creation.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger 
LANGUAGE plpgsql 
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  -- Wrap in exception handler to ensure trigger always succeeds
  BEGIN
    INSERT INTO public.profiles (id, username, full_name, avatar_url, role)
    VALUES (
      new.id,
      -- Ưu tiên lấy username, nếu null thì lấy phần trước @ của email với suffix unique
      COALESCE(
        new.raw_user_meta_data->>'username', 
        split_part(new.email, '@', 1) || '_' || substr(new.id::text, 1, 8)
      ),
      -- Ưu tiên lấy fullname, nếu null thì đặt tạm
      COALESCE(new.raw_user_meta_data->>'full_name', 'Student'),
      new.raw_user_meta_data->>'avatar_url', 
      'student'::user_role -- HARD-CODE: Luôn là student khi mới đăng ký.
    )
    ON CONFLICT (id) DO NOTHING;
  EXCEPTION
    WHEN OTHERS THEN
      -- Log error but don't abort user creation
      RAISE WARNING 'Failed to create profile for user %: %', new.id, SQLERRM;
  END;
  
  RETURN new;
END;
$$;

-- Gắn Trigger vào bảng auth.users
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

