-- 1. ENUMs (Luôn chạy đầu tiên)
CREATE TYPE user_role AS ENUM ('system_admin', 'teacher', 'student');
CREATE TYPE question_status AS ENUM ('pending', 'approved', 'published');
CREATE TYPE question_source AS ENUM ('ai', 'human_edit');

-- 2. Profiles (Độc lập, chỉ phụ thuộc auth.users)
CREATE TABLE public.profiles (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  username TEXT UNIQUE,
  full_name TEXT,
  avatar_url TEXT,
  role user_role DEFAULT 'student',
  
  -- Gamification core
  total_xp INTEGER DEFAULT 0,
  weekly_xp INTEGER DEFAULT 0,
  current_streak INTEGER DEFAULT 0,
  last_study_date DATE,
  freeze_count INTEGER DEFAULT 0,
  
  -- Safety
  daily_limit_minutes INTEGER DEFAULT 30,
  
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Subjects (Độc lập)
CREATE TABLE public.subjects (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code TEXT UNIQUE, 
  name TEXT NOT NULL,
  description TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 4. Nodes (Phụ thuộc Subjects & Profiles) -> ĐẨY LÊN TRƯỚC DOCUMENTS
CREATE TABLE public.nodes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  subject_id BIGINT REFERENCES public.subjects(id) ON DELETE CASCADE,
  
  title TEXT NOT NULL,
  description TEXT,
  
  required_xp INTEGER DEFAULT 50,
  
  parent_node_id BIGINT REFERENCES public.nodes(id), -- Self-referencing OK
  position_x INTEGER,
  position_y INTEGER,
  
  created_by UUID REFERENCES public.profiles(id),
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 5. Documents (Phụ thuộc Subjects & Nodes) -> GIỜ MỚI ĐƯỢC TẠO
CREATE TABLE public.documents (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  
  subject_id BIGINT REFERENCES public.subjects(id),
  
  -- Giờ bảng nodes đã tồn tại, reference OK
  node_id BIGINT REFERENCES public.nodes(id), 
  
  title TEXT NOT NULL,
  file_url TEXT NOT NULL,
  extracted_text TEXT,
  
  created_by UUID REFERENCES public.profiles(id),
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 6. Questions (Phụ thuộc Nodes & Documents)
CREATE TABLE public.questions (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  
  node_id BIGINT REFERENCES public.nodes(id) ON DELETE CASCADE,
  document_id UUID REFERENCES public.documents(id),
  
  content JSONB NOT NULL,
  
  correct_option_index INTEGER NOT NULL
    CHECK (correct_option_index BETWEEN 0 AND 3),
  
  difficulty TEXT CHECK (difficulty IN ('easy','medium','hard')),
  
  status question_status DEFAULT 'pending',
  source question_source DEFAULT 'ai',
  
  created_by UUID REFERENCES public.profiles(id),
  approved_by UUID REFERENCES public.profiles(id),
  approved_at TIMESTAMPTZ,
  
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 7. Quiz Attempts (Phụ thuộc Questions & Profiles)
CREATE TABLE public.quiz_attempts (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  
  user_id UUID REFERENCES public.profiles(id),
  question_id UUID REFERENCES public.questions(id),
  
  selected_option_index INTEGER NOT NULL,
  is_correct BOOLEAN NOT NULL,
  xp_earned INTEGER DEFAULT 0,
  
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Index cho Quiz
CREATE INDEX idx_quiz_user ON public.quiz_attempts(user_id);
CREATE INDEX idx_quiz_time ON public.quiz_attempts(created_at);

-- 8. Student Progress (Phụ thuộc Nodes & Profiles)
CREATE TABLE public.student_progress (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  
  student_id UUID REFERENCES public.profiles(id),
  node_id BIGINT REFERENCES public.nodes(id),
  
  xp_accumulated INTEGER DEFAULT 0,
  is_completed BOOLEAN DEFAULT FALSE,
  
  unlocked_at TIMESTAMPTZ DEFAULT NOW(),
  completed_at TIMESTAMPTZ,
  
  UNIQUE(student_id, node_id)
);

-- 9. Badges & User Badges
CREATE TABLE public.badges (
  id TEXT PRIMARY KEY,
  name TEXT NOT NULL,
  description TEXT,
  icon_url TEXT,
  
  condition_type TEXT, 
  condition_value INTEGER
);

CREATE TABLE public.user_badges (
  user_id UUID REFERENCES public.profiles(id),
  badge_id TEXT REFERENCES public.badges(id),
  earned_at TIMESTAMPTZ DEFAULT NOW(),
  PRIMARY KEY (user_id, badge_id)
);

-- 10. Safety & Logs
CREATE TABLE public.daily_usage (
  user_id UUID REFERENCES public.profiles(id),
  study_date DATE DEFAULT CURRENT_DATE,
  total_seconds INTEGER DEFAULT 0,
  PRIMARY KEY (user_id, study_date)
);

CREATE TABLE public.chat_logs (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  
  user_id UUID REFERENCES public.profiles(id),
  question_id UUID REFERENCES public.questions(id),
  
  sender TEXT CHECK (sender IN ('user','ai')),
  message TEXT NOT NULL,
  
  is_blocked BOOLEAN DEFAULT FALSE,
  
  created_at TIMESTAMPTZ DEFAULT NOW()
);


